/**
 –í–æ-–ø–µ—Ä–≤—ã—Ö. –æ–ø—Ä–µ–¥–µ–ª–∏–º ViewModel, —É –∫–æ—Ç–æ—Ä–æ–π –±—É–¥–µ—Ç –ø–∞—Ä–∞ —Å–≤–æ–π—Å—Ç–≤ –¥–ª—è –≤–≤–æ–¥–∞ –¥–∞–Ω–Ω—ã—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–º  (–∏–º—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è username –∏ –ø–∞—Ä–æ–ª—å password), –∏ –Ω–∞ –¥–∞–Ω–Ω—ã–π –º–æ–º–µ–Ω—Ç –æ–¥–Ω–æ –≤—ã—Ö–æ–¥–Ω–æ–µ (output) —Å–≤–æ–π—Å—Ç–≤–æ isValid –¥–ª—è –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∏—è —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤ –Ω–µ–∫–æ—Ç–æ—Ä–æ–π –±–∏–∑–Ω–µ—Å-–ª–æ–≥–∏–∫–∏ –ø—Ä–æ–≤–µ—Ä–∫–∏ –≤–≤–µ–¥–µ–Ω–Ω—ã—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–º username –∏ password, –∫–æ—Ç–æ—Ä—É—é –º—ã —Å–æ–±–∏—Ä–∞–µ–º—Å—è –≤–Ω–µ–¥—Ä–∏—Ç—å –≤ –±–ª–∏–∂–∞–π—à–µ–µ –≤—Ä–µ–º—è
 */
class UserViewModel: ObservableObject {
    // input
    @Published var username = ""
    @Published var password = ""
    @Published var passwordAgain = ""
    
    // output
    @Published var isValid = false
    @Published var usernameMessage = ""  // –ø—Ä–µ–¥—É–ø—Ä–µ–∂–¥–∞—é—â–∏–µ —Å–æ–æ–±—â–µ–Ω–∏—è
    @Published var passwordMessage = ""  // –ø—Ä–µ–¥—É–ø—Ä–µ–∂–¥–∞—é—â–∏–µ —Å–æ–æ–±—â–µ–Ω–∏—è
    
    
    private var cancellableSet: Set<AnyCancellable> = []
    
    // –Ω–∞—á–Ω–µ–º —Å —Ä–µ–∞–ª–∏–∑–∞—Ü–∏–∏ –∫–æ–Ω–≤–µ–π–µ—Ä–∞ –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ –ø–∞—Ä–æ–ª–µ–π, –≤–≤–µ–¥–µ–Ω–Ω—ã—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–º
    private var isPasswordEmptyPublisher: AnyPublisher<Bool, Never> {
        // (1)
        /**
         –ü—Ä–æ–≤–µ—Ä–∏—Ç—å, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ –ø–∞—Ä–æ–ª—å password –ø—É—Å—Ç—ã–º, –¥–æ–≤–æ–ª—å–Ω–æ –ø—Ä–æ—Å—Ç–æ, –∏ –≤—ã –º–æ–∂–µ—Ç–µ –∑–∞–º–µ—Ç–∏—Ç—å, —á—Ç–æ —ç—Ç–æ—Ç –º–µ—Ç–æ–¥ –æ—á–µ–Ω—å –ø–æ—Ö–æ–∂ –Ω–∞ –Ω–∞—à—É –ø—Ä–µ–¥—ã–¥—É—â—É—é –ø—Ä–æ–≤–µ—Ä–∫—É –∏–º–µ–Ω–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è username.
         –ù–æ –Ω–∞ —ç—Ç–æ—Ç —Ä–∞–∑ –≤–º–µ—Å—Ç–æ –Ω–µ–ø–æ—Å—Ä–µ–¥—Å—Ç–≤–µ–Ω–Ω–æ–≥–æ –ø—Ä–∏—Å–≤–æ–µ–Ω–∏—è —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞ –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏—è –≤—ã—Ö–æ–¥–Ω–æ–º—É —Å–≤–æ–π—Å—Ç–≤—É isValid, –º—ã –≤–æ–∑–≤—Ä–∞—â–∞–µ–º AnyPublisher<Bool, Never>.
         –ú—ã –ø–æ—Å—Ç—É–ø–∞–µ–º —Ç–∞–∫, –ø–æ—Ç–æ–º—É —á—Ç–æ —ç—Ç–æ –ø—Ä–æ–º–µ–∂—É—Ç–æ—á–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –∏ –ø–æ–∑–∂–µ –º—ã –±—É–¥–µ–º –æ–±—ä–µ–¥–∏–Ω—è—Ç—å –Ω–µ—Å–∫–æ–ª—å–∫–æ –Ω–∞—à–∏—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å—Å–∫–∏—Ö –∏–∑–¥–∞—Ç–µ–ª–µ–π Publishers  –≤ –º–Ω–æ–≥–æ—Å—Ç—É–ø–µ–Ω—á–∞—Ç—É—é —Ü–µ–ø–æ—á–∫—É,
         –ø—Ä–µ–∂–¥–µ —á–µ–º –ø–æ–¥–ø–∏—à–µ–º—Å—è –Ω–∞ –∫–æ–Ω–µ—á–Ω—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç (–ø—Ä–∞–≤–∏–ª—å–Ω—ã–π –∏–ª–∏ –Ω–µ—Ç).
         */
        $password
            .debounce(for: 0.8, scheduler: RunLoop.main)
            .removeDuplicates()
            .map { password in
                return password == ""
            }
            .eraseToAnyPublisher()
            /**
             –í —Å–ª—É—á–∞–µ, –µ—Å–ª–∏ –≤–∞–º –∏–Ω—Ç–µ—Ä–µ—Å–Ω–æ, –ø–æ—á–µ–º—É –º—ã –¥–æ–ª–∂–Ω—ã –≤—ã–∑—ã–≤–∞—Ç—å eraseToAnyPublisher () –≤ –∫–æ–Ω—Ü–µ –∫–∞–∂–¥–æ–π —Ü–µ–ø–æ—á–∫–∏, –ø–æ—è—Å–Ω—è—é: —ç—Ç–æ –≤—ã–ø–æ–ª–Ω—è–µ—Ç —Å—Ç–∏—Ä–∞–Ω–∏–µ –Ω–µ–∫–æ—Ç–æ—Ä–æ–≥–æ –¢–ò–ü–ê, –æ–Ω–æ –≥–∞—Ä–∞–Ω—Ç–∏—Ä—É–µ—Ç, —á—Ç–æ –º—ã –Ω–µ –ø–æ–ª—É—á–∏–º –Ω–µ–∫–æ—Ç–æ—Ä—ã–µ —Å—É–º–∞—Å—à–µ–¥—à–∏–µ –≤–ª–æ–∂–µ–Ω–Ω—ã–µ –¢–ò–ü–´ –≤–æ–∑–≤—Ä–∞—Ç–∞ –∏ —Å–º–æ–∂–µ–º –∏—Ö –≤—Å—Ç—Ä–æ–∏—Ç—å –≤ –ª—é–±—É—é —Ü–µ–ø–æ—á–∫—É.
             */
    }

    /**
     –ß—Ç–æ–±—ã –ø—Ä–æ–≤–µ—Ä–∏—Ç—å, —Å–æ–¥–µ—Ä–∂–∞—Ç –ª–∏ –¥–≤–∞ –æ—Ç–¥–µ–ª—å–Ω—ã—Ö —Å–≤–æ–π—Å—Ç–≤–∞ –æ–¥–∏–Ω–∞–∫–æ–≤—ã–µ —Å—Ç—Ä–æ–∫–∏ String, –º—ã –∏—Å–ø–æ–ª—å–∑—É–µ–º –æ–ø–µ—Ä–∞—Ç–æ—Ä CombineLatest.
     –ü–æ–º–Ω–∏—Ç–µ, —á—Ç–æ —Å–≤–æ–π—Å—Ç–≤–∞, –ø—Ä–∏–≤—è–∑–∞–Ω–Ω—ã–µ –∫ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–∏–º SecureField, –∑–∞–ø—É—Å–∫–∞—é—Ç—Å—è –∫–∞–∂–¥—ã–π —Ä–∞–∑, –∫–æ–≥–¥–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –≤–≤–æ–¥–∏—Ç —Å–∏–º–≤–æ–ª,
     –∏ –º—ã —Ö–æ—Ç–∏–º —Å—Ä–∞–≤–Ω–∏–≤–∞—Ç—å –ø–æ—Å–ª–µ–¥–Ω–∏–µ –∑–Ω–∞—á–µ–Ω–∏—è –¥–ª—è –∫–∞–∂–¥–æ–≥–æ –∏–∑ —ç—Ç–∏—Ö –ø–æ–ª–µ–π. –û–ø–µ—Ä–∞—Ç–æ—Ä CombineLatest –ø–æ–∑–≤–æ–ª—è–µ—Ç –Ω–∞–º —ç—Ç–æ –¥–µ–ª–∞—Ç—å.
     */
    private var arePasswordsEqualPublisher: AnyPublisher<Bool, Never> {
        Publishers.CombineLatest($password, $passwordAgain)
            .debounce(for: 0.2, scheduler: RunLoop.main)
            .map { password, passwordAgain in
                return password == passwordAgain
            }
            .eraseToAnyPublisher()
    }
    
    
    /**
     –î–ª—è —Ä–∞—Å—á–µ—Ç–∞ –Ω–∞–¥–µ–∂–Ω–æ—Å—Ç–∏ –ø–∞—Ä–æ–ª—è –º—ã –∏—Å–ø–æ–ª—å–∑—É–µ–º —Ñ—Ä–µ–π–º–≤–æ—Ä–∫ Navajo Swift –∏ –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ–º —Ä–µ–∑—É–ª—å—Ç–∏—Ä—É—é—â–µ–µ –ø–µ—Ä–µ—á–∏—Å–ª–µ–Ω–∏–µ enum
     –≤ Bool —Å –ø–æ–º–æ—â—å—é —Ü–µ–ø–æ—á–∫–∏ –≤ –¥—Ä—É–≥–æ–º –∏–∑–¥–∞—Ç–µ–ª–µ isPasswordStrongEnoughPublisher. –í —ç—Ç–æ–º –∫–æ–¥–µ –º—ã –≤–ø–µ—Ä–≤—ã–µ –ø–æ–¥–ø–∏—Å—ã–≤–∞–µ–º—Å—è (subscribe) –Ω–∞ –æ–¥–∏–Ω –∏–∑ –Ω–∞—à–∏—Ö —Å–æ–±—Å—Ç–≤–µ–Ω–Ω—ã—Ö –∏–∑–¥–∞—Ç–µ–ª–µ–π passwordStrengthPublisher, –∏ —ç—Ç–æ –æ—á–µ–Ω—å –Ω–∞–≥–ª—è–¥–Ω–æ –ø–æ–∫–∞–∑—ã–≤–∞–µ—Ç, –∫–∞–∫ –º—ã –º–æ–∂–µ–º –∫–æ–º–±–∏–Ω–∏—Ä–æ–≤–∞—Ç—å
     –º–Ω–æ–∂–µ—Å—Ç–≤–æ –∏–∑–¥–∞—Ç–µ–ª–µ–π (–Ω–∞—à–∏—Ö —Å–æ–±—Å—Ç–≤–µ–Ω–Ω—ã—Ö –∏ –≤—Å—Ç—Ä–æ–µ–Ω–Ω—ã—Ö –≤ Combine) –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è —Ç—Ä–µ–±—É–µ–º–æ–≥–æ –≤—ã—Ö–æ–¥–∞ (output).
     
     –í —Å–ª—É—á–∞–µ, –µ—Å–ª–∏ –≤–∞–º –∏–Ω—Ç–µ—Ä–µ—Å–Ω–æ, –ø–æ—á–µ–º—É –º—ã –¥–æ–ª–∂–Ω—ã –≤—ã–∑—ã–≤–∞—Ç—å eraseToAnyPublisher () –≤ –∫–æ–Ω—Ü–µ –∫–∞–∂–¥–æ–π —Ü–µ–ø–æ—á–∫–∏, –ø–æ—è—Å–Ω—è—é:
     —ç—Ç–æ –≤—ã–ø–æ–ª–Ω—è–µ—Ç —Å—Ç–∏—Ä–∞–Ω–∏–µ –Ω–µ–∫–æ—Ç–æ—Ä–æ–≥–æ –¢–ò–ü–ê, –æ–Ω–æ –≥–∞—Ä–∞–Ω—Ç–∏—Ä—É–µ—Ç, —á—Ç–æ –º—ã –Ω–µ –ø–æ–ª—É—á–∏–º –Ω–µ–∫–æ—Ç–æ—Ä—ã–µ —Å—É–º–∞—Å—à–µ–¥—à–∏–µ –≤–ª–æ–∂–µ–Ω–Ω—ã–µ –¢–ò–ü–´ –≤–æ–∑–≤—Ä–∞—Ç–∞ –∏ —Å–º–æ–∂–µ–º –∏—Ö –≤—Å—Ç—Ä–æ–∏—Ç—å –≤ –ª—é–±—É—é —Ü–µ–ø–æ—á–∫—É.
     */
    private var passwordStrengthPublisher: AnyPublisher<PasswordStrength, Never> {
        $password
            .debounce(for: 0.2, scheduler: RunLoop.main)
            .removeDuplicates()
            .map { input in
                return Navajo.strength(ofPassword: input)  // (2)
            }
            .eraseToAnyPublisher()
    }

    private var isPasswordStrongEnoughPublisher: AnyPublisher<Bool, Never> {
        passwordStrengthPublisher
            .map { strength in
                switch strength {
                case .reasonable, .strong, .veryStrong:
                    return true
                default:
                    return false
                }
            }
            .eraseToAnyPublisher()
    }
    
    
    /**
     –û—Ç–ª–∏—á–Ω–æ ‚Äî —Ç–µ–ø–µ—Ä—å –º—ã –º–Ω–æ–≥–æ –∑–Ω–∞–µ–º –æ –ø–∞—Ä–æ–ª—è—Ö, –≤–≤–µ–¥–µ–Ω–Ω—ã—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–º, –¥–∞–≤–∞–π—Ç–µ —Å–≤–µ–¥–µ–º —ç—Ç–æ –∫ –æ–¥–Ω–æ–π –≤–µ—â–∏, –∫–æ—Ç–æ—Ä—É—é –º—ã –¥–µ–π—Å—Ç–≤–∏—Ç–µ–ª—å–Ω–æ —Ö–æ—Ç–∏–º –∑–Ω–∞—Ç—å: —ç—Ç–æ—Ç  –ø–∞—Ä–æ–ª—å password ‚Äî –ø—Ä–∞–≤–∏–ª—å–Ω—ã–π?
     –ö–∞–∫ –≤—ã —É–∂–µ –¥–æ–≥–∞–¥–∞–ª–∏—Å—å, –Ω–∞–º –Ω—É–∂–Ω–æ –±—É–¥–µ—Ç –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –æ–ø–µ—Ä–∞—Ç–æ—Ä CombineLatest, –Ω–æ –ø–æ—Å–∫–æ–ª—å–∫—É –Ω–∞ —ç—Ç–æ—Ç —Ä–∞–∑ —É –Ω–∞—Å –µ—Å—Ç—å —Ç—Ä–∏ –ø–∞—Ä–∞–º–µ—Ç—Ä–∞, –º—ã –±—É–¥–µ–º –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å CombineLatest3, –∫–æ—Ç–æ—Ä—ã–π –ø—Ä–∏–Ω–∏–º–∞–µ—Ç —Ç—Ä–∏ –≤—Ö–æ–¥–Ω—ã—Ö –ø–∞—Ä–∞–º–µ—Ç—Ä–∞
     
     –û—Å–Ω–æ–≤–Ω–∞—è –ø—Ä–∏—á–∏–Ω–∞, –ø–æ –∫–æ—Ç–æ—Ä–æ–π –º—ã –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ–º —Ç—Ä–∏ –ª–æ–≥–∏—á–µ—Å–∫–∏—Ö –∑–Ω–∞—á–µ–Ω–∏—è –≤ –µ–¥–∏–Ω—Å—Ç–≤–µ–Ω–Ω–æ–µ –ø–µ—Ä–µ—á–∏—Å–ª–µ–Ω–∏–µ, –∑–∞–∫–ª—é—á–∞–µ—Ç—Å—è –≤ —Ç–æ–º, —á—Ç–æ –º—ã —Ö–æ—Ç–∏–º –∏–º–µ—Ç—å –≤–æ–∑–º–æ–∂–Ω–æ—Å—Ç—å –≤—ã–¥–∞–≤–∞—Ç—å –ø–æ–¥—Ö–æ–¥—è—â–µ–µ –ø—Ä–µ–¥—É–ø—Ä–µ–∂–¥–∞—é—â–µ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞ –ø—Ä–æ–≤–µ—Ä–∫–∏.
     –°–∫–∞–∑–∞—Ç—å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é, —á—Ç–æ –µ–≥–æ –ø–∞—Ä–æ–ª—å password –Ω–µ –≥–æ–¥–∏—Ç—Å—è, –Ω–µ –æ—á–µ–Ω—å –∏–Ω—Ñ–æ—Ä–º–∞—Ç–∏–≤–Ω–æ, –Ω–µ —Ç–∞–∫ –ª–∏? –ù–∞–º–Ω–æ–≥–æ –ª—É—á—à–µ, –µ—Å–ª–∏ –º—ã —Å–∫–∞–∂–µ–º –µ–º—É, –ø–æ—á–µ–º—É —ç—Ç–æ—Ç –ø–∞—Ä–æ–ª—å password –Ω–µ –ø–æ–¥—Ö–æ–¥–∏—Ç.
     */
    enum PasswordCheck {
        case valid
        case empty
        case noMatch
        case notStrongEnough
    }
    
    private var isPasswordValidPublisher: AnyPublisher<PasswordCheck, Never> {
        Publishers.CombineLatest3(isPasswordEmptyPublisher, arePasswordsEqualPublisher, isPasswordStrongEnoughPublisher)
            .map { passwordIsEmpty, passwordsAreEqual, passwordIsStrongEnough in
                if passwordIsEmpty {
                    return .empty
                } else if !passwordsAreEqual {
                    return .noMatch
                } else if !passwordIsStrongEnough {
                    return .notStrongEnough
                } else {
                    return .valid
                }
            }
            .eraseToAnyPublisher()
    }
    
    /**
     –ß—Ç–æ–±—ã —É—Å—Ç–∞–Ω–æ–≤–∏—Ç—å, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ –∏–º—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è username –ø—Ä–∞–≤–∏–ª—å–Ω—ã–º,
     –º—ã –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –≤–≤–µ–¥–µ–Ω–Ω–æ–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–º –∏–º—è –∏–∑ String –≤ Bool, –∏—Å–ø–æ–ª—å–∑—É—è –æ–ø–µ—Ä–∞—Ç–æ—Ä map.
     
     –†–µ–∑—É–ª—å—Ç–∞—Ç —ç—Ç–æ–≥–æ –ø—Ä–µ–æ–±—Ä–∞–∑–æ–≤–∞–Ω–∏—è –∑–∞—Ç–µ–º –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è  –ø–æ–¥–ø–∏—Å—á–∏–∫–æ–º  assign, –∫–æ—Ç–æ—Ä—ã–π ‚Äî –∫–∞–∫ —Å–ª–µ–¥—É–µ—Ç –∏–∑ –Ω–∞–∑–≤–∞–Ω–∏—è ‚Äî
     –Ω–∞–∑–Ω–∞—á–∞–µ—Ç –ø–æ–ª—É—á–µ–Ω–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ value –≤—ã—Ö–æ–¥–Ω–æ–º—É (output) —Å–≤–æ–π—Å—Ç–≤—É isValid –Ω–∞—à–µ–π –ú–æ–¥–µ–ª–∏ UserViewModel.
     
     –û–ø–µ—Ä–∞—Ç–æ—Ä debounce –ø–æ–∑–≤–æ–ª—è–µ—Ç –Ω–∞–º —Å–æ–æ–±—â–∏—Ç—å —Å–∏—Å—Ç–µ–º–µ, —á—Ç–æ –º—ã —Ö–æ—Ç–∏–º –¥–æ–∂–¥–∞—Ç—å—Å—è –ø–∞—É–∑—ã –≤ –¥–æ—Å—Ç–∞–≤–∫–µ —Å–æ–±—ã—Ç–∏–π, –Ω–∞–ø—Ä–∏–º–µ—Ä, –∫–æ–≥–¥–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –ø–µ—Ä–µ—Å—Ç–∞–µ—Ç –ø–µ—á–∞—Ç–∞—Ç—å.
     
     –ê–Ω–∞–ª–æ–≥–∏—á–Ω–æ, –æ–ø–µ—Ä–∞—Ç–æ—Ä removeDuplicates –±—É–¥–µ—Ç –ø—É–±–ª–∏–∫–æ–≤–∞—Ç—å —Å–æ–±—ã—Ç–∏—è, —Ç–æ–ª—å–∫–æ –µ—Å–ª–∏ –æ–Ω–∏ –æ—Ç–ª–∏—á–∞—é—Ç—Å—è –æ—Ç –ª—é–±—ã—Ö –ø—Ä–µ–¥—ã–¥—É—â–∏—Ö —Å–æ–±—ã—Ç–∏–π.
     –ù–∞–ø—Ä–∏–º–µ—Ä, –µ—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å —Å–Ω–∞—á–∞–ª–∞ –≤–≤–æ–¥–∏—Ç john, –∑–∞—Ç–µ–º joe, –∞ –∑–∞—Ç–µ–º —Å–Ω–æ–≤–∞ john, –º—ã –ø–æ–ª—É—á–∏–º john —Ç–æ–ª—å–∫–æ –æ–¥–∏–Ω —Ä–∞–∑. –≠—Ç–æ –ø–æ–º–æ–≥–∞–µ—Ç —Å–¥–µ–ª–∞—Ç—å –Ω–∞—à UI –±–æ–ª–µ–µ —ç—Ñ—Ñ–µ–∫—Ç–∏–≤–Ω—ã–º.
     
     –†–µ–∑—É–ª—å—Ç–∞—Ç —ç—Ç–æ–π —Ü–µ–ø–æ—á–∫–∏ –≤—ã–∑–æ–≤–æ–≤ —è–≤–ª—è–µ—Ç—Å—è Cancellable, –∫–æ—Ç–æ—Ä—ã–π –º—ã –º–æ–∂–µ–º –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å –¥–ª—è –æ—Ç–º–µ–Ω—ã –æ–±—Ä–∞–±–æ—Ç–∫–∏ –ø—Ä–∏ –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ—Å—Ç–∏ (–ø–æ–ª–µ–∑–Ω–æ –¥–ª—è –¥–ª–∏–Ω–Ω—ã—Ö —Ü–µ–ø–æ—á–µ–∫).
     –ú—ã —Å–æ—Ö—Ä–∞–Ω–∏–º –µ–≥–æ (–∏ –≤—Å–µ –æ—Å—Ç–∞–ª—å–Ω—ã–µ, –∫–æ—Ç–æ—Ä—ã–µ –º—ã —Å–æ–∑–¥–∞–¥–∏–º –ø–æ–∑–∂–µ) –≤ –º–Ω–æ–∂–µ—Å—Ç–≤–µ Set <AnyCancellable>, –∫–æ—Ç–æ—Ä–æ–µ –º—ã –º–æ–∂–µ–º –æ—á–∏—Å—Ç–∏—Ç—å –ø—Ä–∏ deinit –Ω–∞—à–µ–≥–æ userViewModel.
     */
    private var isUsernameValidPublisher: AnyPublisher<Bool, Never> {
        $username
            .debounce(for: 0.8, scheduler: RunLoop.main)
            .removeDuplicates()
            .map { input in
                print(input)
                return input.count >= 3
            }
            .eraseToAnyPublisher()
    }
    
    private var isFormValidPublisher: AnyPublisher<Bool, Never> {
        Publishers.CombineLatest(isUsernameValidPublisher, isPasswordValidPublisher)
            .map { userNameIsValid, passwordIsValid in
                return userNameIsValid && (passwordIsValid == .valid)
            }
            .eraseToAnyPublisher()
    }
    
    init() {
        /**
         –ø–æ–º–æ–∂–µ–º –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é –ø—Ä–∏ –∑–∞–ø–æ–ª–Ω–µ–Ω–∏–∏ —Ñ–æ—Ä–º—ã —Ä–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏–∏.
         –°–Ω–∞—á–∞–ª–∞ –º—ã –ø–æ–¥–ø–∏—Å—ã–≤–∞–µ–º—Å—è subscribe –Ω–∞ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–∏—Ö —É–∂–µ –ø–æ–ª—É—á–µ–Ω–Ω—ã—Ö –∏–∑–¥–∞—Ç–µ–ª–µ–π Publishers, —á—Ç–æ–±—ã —É–∑–Ω–∞—Ç—å,
         –∫–æ–≥–¥–∞ —Å–≤–æ–π—Å—Ç–≤–∞ username –∏ password –Ω–µ–ø—Ä–∞–≤–∏–ª—å–Ω—ã–µ (invalid).
         –û–ø—è—Ç—å –∂–µ, –Ω–∞–º –Ω—É–∂–Ω–æ —É–±–µ–¥–∏—Ç—å—Å—è, —á—Ç–æ —ç—Ç–æ –ø—Ä–æ–∏—Å—Ö–æ–¥–∏—Ç –Ω–∞ main –ø–æ—Ç–æ–∫–µ, —Ç–æ –µ—Å—Ç—å –Ω–∞ –ø–æ—Ç–æ–∫–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å—Å–∫–æ–≥–æ –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å–∞, –ø
         –æ—ç—Ç–æ–º—É –º—ã –≤—ã–∑–æ–≤–µ–º –º–µ—Ç–æ–¥ receive (on üôÇ –∏ –ø–µ—Ä–µ–¥–∞–¥–∏–º —Ç—É–¥–∞ –∑–Ω–∞—á–µ–Ω–∏–µ RunLoop.main.
         */
        isUsernameValidPublisher
            .receive(on: RunLoop.main)
            .map { valid in
                valid ? "": "User name must at least have 3 characters"
            }
            .assign(to: \.usernameMessage, on: self)
            .store(in: &cancellableSet)
        
        isPasswordValidPublisher
            .receive(on: RunLoop.main)
            .map { passwordCheck in
                switch passwordCheck {
                case .empty:
                    return "Password must not be empty"
                case .noMatch:
                    return "Password don't match"
                case .notStrongEnough:
                    return "Password not strong enough"
                default:
                    return ""
                }
            }
            .assign(to: \.passwordMessage, on: self)
            .store(in: &cancellableSet)
        

        /**
         –í—Å–µ, —á—Ç–æ –º—ã –ø—Ä–æ–¥–µ–ª–∞–ª–∏ –¥–æ —ç—Ç–æ–≥–æ,  —Å–æ–≤–µ—Ä—à–µ–Ω–Ω–æ –±–µ—Å–ø–æ–ª–µ–∑–Ω–æ, –µ—Å–ª–∏ –Ω–µ –ø–æ–¥–∫–ª—é—á–∏—Ç—å—Å—è –∫ UI. –ß—Ç–æ–±—ã —É–ø—Ä–∞–≤–ª—è—Ç—å —Å–æ—Å—Ç–æ—è–Ω–∏–µ–º –∫–Ω–æ–ø–∫–∏ ¬´Sign up¬´,
         –Ω–∞–º –Ω—É–∂–Ω–æ –æ–±–Ω–æ–≤–∏—Ç—å –≤—ã—Ö–æ–¥–Ω–æ–µ —Å–≤–æ–π—Å—Ç–≤–æ isValid –≤ –Ω–∞—à–µ–π –ú–æ–¥–µ–ª–∏ UserViewModel.
         –î–ª—è —ç—Ç–æ–≥–æ –º—ã –ø—Ä–æ—Å—Ç–æ –ø–æ–¥–ø–∏—Å—ã–≤–∞–µ–º—Å—è –Ω–∞ isFormValidPublisher –∏ –ø—Ä–∏—Å–≤–∞–∏–≤–∞–µ–º –∑–Ω–∞—á–µ–Ω–∏—è, –∫–æ—Ç–æ—Ä—ã–µ –æ–Ω –ø—É–±–ª–∏–∫—É–µ—Ç, —Å–≤–æ–π—Å—Ç–≤—É isValid
         
         –ü–æ—Å–∫–æ–ª—å–∫—É —ç—Ç–æ—Ç –∫–æ–¥ –≤–∑–∞–∏–º–æ–¥–µ–π—Å—Ç–≤—É–µ—Ç —Å UI, –æ–Ω –¥–æ–ª–∂–µ–Ω —Ä–∞–±–æ—Ç–∞—Ç—å –Ω–∞ main –ø–æ—Ç–æ–∫–µ.
         –ú—ã –º–æ–∂–µ–º –∑–∞—Å—Ç–∞–≤–∏—Ç—å SwiftUI –≤—ã–ø–æ–ª–Ω–∏—Ç—å —ç—Ç–æ—Ç –∫–æ–¥ –Ω–∞ main –ø–æ—Ç–æ–∫–µ, –ø–æ—Ç–æ–∫–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å—Å–∫–æ–≥–æ –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å–∞, –≤—ã–∑–≤–∞–≤ –º–µ—Ç–æ–¥ receive (on: RunLoop.main).
         */
        isFormValidPublisher
            .receive(on: RunLoop.main)
            .assign(to: \.isValid, on: self)
            .store(in: &cancellableSet)
    }
    
    
    
    
